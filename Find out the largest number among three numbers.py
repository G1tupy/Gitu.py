def find_largest(num1, num2, num3):
    largest = max(num1, num2, num3)
    print(f"The largest number is: {largest}")# Here is the largest number is 50
# Get user input for three numbers
user_num1 = float(input("Enter the first number: "))#20
user_num2 = float(input("Enter the second number: "))#30
user_num3 = float(input("Enter the third number: "))#50
# Find the largest number
find_largest(user_num1, user_num2, user_num3)
